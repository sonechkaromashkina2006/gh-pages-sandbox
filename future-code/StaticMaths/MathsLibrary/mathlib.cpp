
#include "mathlib.h"

namespace MathLibrary{
	double Arithmetic::add(double a, double b) {
		return a + b;
	}
	double Arithmetic::substruct(double a, double b) {
		return a - b;
	}
	double Arithmetic::multiply(double a, double b) {
		return a * b;
	}
	double Arithmetic::division(double a, double b) {
		return a / b;
	}
}